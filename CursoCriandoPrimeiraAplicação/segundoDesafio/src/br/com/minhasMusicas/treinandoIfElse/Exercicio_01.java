package br.com.minhasMusicas.treinandoIfElse;

//Ana precisa identificar se um número é par ou ímpar para um jogo que está desenvolvendo.
//Para isso, deseja criarum programa que analisa se o número que ela definiu previamente é par ou ímpar.
public class Exercicio_01 {
    public static void main(String[] args) {

        int numero = 6;

        if (numero % 2 == 0) {
            System.out.println("Numero é igual a par !");
        } else {
            System.out.println("Numero é impar !");
        }


    }
}
